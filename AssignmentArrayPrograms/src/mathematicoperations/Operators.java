package mathematicoperations;

public class Operators {

	public static void main(String[] args) {
		int a=4;
		int b=6;
		
		System.out.println("a & b =" +(a&b));
		System.out.println("a | b =" +(a|b));
		System.out.println("a ^ b =" +(a^b));
		System.out.println("~a =" +(~a)); // formula -(a+1)
		System.out.println("~b =" +(~b)); // formula -(b+1)
		System.out.println("a<<1 =" +(a<<1));
		System.out.println("a>>b =" +(a>>b));
		System.out.println("a>>b =" +(a>>b));
		System.out.println("a<<b =" +(a<<b));
		System.out.println("a>>>b =" +(a>>>b));// it will consider 0 something ..cjeck this 
		

	}

}
